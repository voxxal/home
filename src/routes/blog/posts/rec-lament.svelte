<p><a href="http://worrydream.com/refs/Lockhart-MathematiciansLament.pdf">A mathematician's lament</a> is, well, a lament. A mathematician complaining about the current state of math education in schools. It is incredibly eye opening to hear about how a mathematician feels about how math is taught. Lockheart describes math as an art, which can't be taught using formulas. He argues that creativity and actual problem solving is crucial to math, and schools can't just skip over cultivating those skills. </p>
<p>My only complaint about the piece is he never really tries to provide any solutions to the problem. It really is nothing more than a lament. For this reason I will be giving <b>a mathematician's lament</b> a score of 4/5 ★★★★☆.</p>
<p>I really don't have much more to say, I highly recommend giving it a read, especially if you hate math.</p>
