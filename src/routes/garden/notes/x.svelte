<h2>The X Window System</h2>
<p>
  The X Window System, or X11, is a windowing system that uses a client-server model. The X server
  collects user input and sends that to the many clients which are the applications using the X
  server to draw to the screen. X was designed to handle clients that do not originate from the host
  computer meaning that remote applications are able to draw to the screen
</p>
